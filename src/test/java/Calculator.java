public class Calculator {

    public int returnSumOfTwoNumbers(int num1, int num2) {
        int sum = num1 + num2;
        return sum;
    }

    public int returnMultiplicationOfTwoNumbers(int num1, int num2) {
        int multiplication = num1 * num2;
        return multiplication;
    }

    public int returnSubtractionOfTwoNumbers(int num1, int num2) {
        int subtraction= num1 - num2;
        return subtraction;
    }

    public int returnDivisionOfTwoNumbers(int num1, int num2) {
        int division = num1 / num2;
        return division;
    }
}
